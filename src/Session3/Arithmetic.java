package Session3;

public class Arithmetic {


	public static void Addition(int a, int b, int c)//Static Method Declaration with argument (or) Variables
	{
		int i = a+b+c;
		
		System.out.println("The Additional Value is :" + i); // Printing 
	}
	
	public static void Subtraction(int S, int B)
	{
		int x = S-B;
		
		System.out.println("The Subtraction Value is :" + x);
	}
	
	public static void Multiplication(int x, int y, int z)
	{
		int A = x*y*z;
		
		System.out.println("The Multiplication Value is :" + A);
	}
}
